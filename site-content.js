(function applySiteContent() {
  const locale = document.documentElement.lang === "en" ? "en" : "ja";
  const content = window.siteContentData?.[locale];
  if (!content) return;

  const setText = (selector, value) => {
    const element = document.querySelector(selector);
    if (element && value != null) element.textContent = value;
  };

  const setMeta = (selector, value) => {
    const element = document.querySelector(selector);
    if (element && value != null) element.setAttribute("content", value);
  };

  document.title = content.meta.title;
  setMeta('meta[name="description"]', content.meta.description);
  setMeta('meta[name="keywords"]', content.meta.keywords);
  setMeta('meta[property="og:title"]', content.meta.ogTitle);
  setMeta('meta[property="og:description"]', content.meta.ogDescription);
  setMeta('meta[name="twitter:title"]', content.meta.twitterTitle);
  setMeta('meta[name="twitter:description"]', content.meta.twitterDescription);

  const libraryLink = document.querySelector('.topbar-links [data-nav="library"]');
  const songBrowserLink = document.querySelector('.topbar-links [data-nav="song-browser"]');
  if (libraryLink) libraryLink.textContent = content.nav.library;
  if (songBrowserLink) songBrowserLink.textContent = content.nav.songBrowser;
  document.querySelectorAll(".language-switch").forEach((el) => el.setAttribute("aria-label", content.nav.langLabel));

  setText(".hero-copy .eyebrow", content.hero.eyebrow);
  setText(".hero-copy h1", content.hero.title);
  const heroLead = document.querySelector(".hero-copy .lead");
  if (heroLead) {
    heroLead.textContent = content.hero.lead;
    heroLead.hidden = !content.hero.lead;
  }

  const heroButtons = document.querySelectorAll(".hero-actions a");
  if (heroButtons[0]) heroButtons[0].textContent = content.hero.primaryCta;
  if (heroButtons[1]) heroButtons[1].textContent = content.hero.secondaryCta;
  if (heroButtons[2]) heroButtons[2].textContent = content.hero.tertiaryCta;

  const metricItems = document.querySelectorAll(".hero-metrics > div");
  metricItems.forEach((item, index) => {
    item.hidden = index >= content.hero.metrics.length;
  });
  content.hero.metrics.forEach((metric, index) => {
    const item = metricItems[index];
    if (!item) return;
    const dt = item.querySelector("dt");
    const dd = item.querySelector("dd");
    if (dt) dt.textContent = metric[0];
    if (dd) dd.textContent = metric[1];
  });

  const sectionHeadings = document.querySelectorAll(".section-heading");
  if (sectionHeadings[0]) {
    const kicker = sectionHeadings[0].querySelector(".section-kicker");
    const title = sectionHeadings[0].querySelector("h2");
    if (kicker) kicker.textContent = content.about.kicker;
    if (title) title.textContent = content.about.title;
  }
  if (sectionHeadings[1]) {
    const kicker = sectionHeadings[1].querySelector(".section-kicker");
    const title = sectionHeadings[1].querySelector("h2");
    const body = sectionHeadings[1].querySelector(".section-copy");
    if (kicker) kicker.textContent = content.library.kicker;
    if (title) title.textContent = content.library.title;
    if (body) body.textContent = content.library.body;
  }
  if (sectionHeadings[2]) {
    const kicker = sectionHeadings[2].querySelector(".section-kicker");
    const title = sectionHeadings[2].querySelector("h2");
    const body = sectionHeadings[2].querySelector(".section-copy");
    if (kicker) kicker.textContent = content.songBrowser.kicker;
    if (title) title.textContent = content.songBrowser.title;
    if (body) body.textContent = content.songBrowser.body;
  }

  const introCards = document.querySelectorAll(".intro-grid .info-card");
  content.about.cards.forEach((card, index) => {
    const item = introCards[index];
    if (!item) return;
    const h3 = item.querySelector("h3");
    const p = item.querySelector("p");
    if (h3) h3.textContent = card.title;
    if (p) p.textContent = card.body;
  });

  const toolbar = document.querySelector(".toolbar");
  if (toolbar) toolbar.setAttribute("aria-label", content.library.toolbarLabel);
  const songBrowserToolbar = document.querySelector(".song-browser .toolbar");
  if (songBrowserToolbar) songBrowserToolbar.setAttribute("aria-label", content.songBrowser.toolbarLabel);

  const faqSection = document.querySelector("#faq");
  if (faqSection) {
    const kicker = faqSection.querySelector(".section-kicker");
    const title = faqSection.querySelector("h2");
    if (kicker) kicker.textContent = content.faq.kicker;
    if (title) title.textContent = content.faq.title;
    const faqDetails = faqSection.querySelectorAll("details");
    content.faq.items.forEach((item, index) => {
      const detail = faqDetails[index];
      if (!detail) return;
      const summary = detail.querySelector("summary");
      const p = detail.querySelector("p");
      if (summary) summary.textContent = item.q;
      if (p) p.textContent = item.a;
    });
  }

  const ctaCard = document.querySelector(".cta-card");
  if (ctaCard) {
    const kicker = ctaCard.querySelector(".section-kicker");
    const title = ctaCard.querySelector("h2");
    const body = ctaCard.querySelector("p:last-of-type");
    if (kicker) kicker.textContent = content.cta.kicker;
    if (title) title.textContent = content.cta.title;
    if (body) {
      body.textContent = content.cta.body;
      body.hidden = !content.cta.body;
    }
    const buttons = ctaCard.querySelectorAll(".cta-actions a");
    if (buttons[0]) buttons[0].textContent = content.cta.primary;
    if (buttons[1]) buttons[1].textContent = content.cta.secondary;
  }

  const footer = document.querySelector("footer");
  if (footer) {
    const paragraphs = footer.querySelectorAll("p");
    if (paragraphs[0]) {
      paragraphs[0].textContent = content.footer.title;
    }
    const footerLinks = footer.querySelectorAll('a[href="./index.html"], a[href="./en.html"]');
    if (footerLinks[0]) footerLinks[0].textContent = content.footer.ja;
    if (footerLinks[1]) footerLinks[1].textContent = content.footer.en;
  }
})();
